async function main() {
  console.log("üå± Seeding database...")

  // ‚îÄ‚îÄ Admin user ‚îÄ‚îÄ
  console.log("Creating admin user...")
  const adminPassword = await bcrypt.hash("Admin123!", 12)
  await prisma.user.upsert({
    where: { email: "admin@vrijwilligersmatch.nl" },
    update: {},
    create: {
      name: "Platform Admin",
      email: "admin@vrijwilligersmatch.nl",
      password: adminPassword,
      role: "ADMIN",
      status: "ACTIVE",
      onboarded: true,
    },
  })
  console.log("  ‚úÖ admin@vrijwilligersmatch.nl  /  Admin123!")

  // Create categories
  console.log("Creating categories...")
  const categoryMap: Record<string, string> = {}
  for (const cat of CATEGORIES_DATA) {
    const created = await prisma.category.upsert({
      where: { name: cat.name },
      update: {},
      create: cat,
    })
    categoryMap[cat.name] = created.id
  }

  // Create skills
  console.log("Creating skills...")
  const skillMap: Record<string, string> = {}
  for (const skillName of SKILLS_DATA) {
    const created = await prisma.skill.upsert({
      where: { name: skillName },
      update: {},
      create: { name: skillName },
    })
    skillMap[skillName] = created.id
  }

  // Create organisations and vacancies
  console.log("Creating organisations and vacancies...")
  let orgCount = 0
  let vacancyCount = 0

  for (const orgData of ORGANISATIONS) {
    // Create admin user for organisation
    const adminUser = await prisma.user.upsert({
      where: { email: orgData.email },
      update: {},
      create: {
        name: `Admin ${orgData.name}`,
        email: orgData.email,
        role: "ORGANISATION",
        onboarded: true,
      },
    })

    // Create organisation
    const org = await prisma.organisation.upsert({
      where: { slug: orgData.slug },
      update: {},
      create: {
        name: orgData.name,
        slug: orgData.slug,
        description: orgData.description,
        website: orgData.website,
        email: orgData.email,
        phone: orgData.phone,
        address: orgData.address,
        city: orgData.city,
        status: "APPROVED",
        adminId: adminUser.id,
      },
    })

    // Add organisation categories
    for (const catName of orgData.categories) {
      if (categoryMap[catName]) {
        await prisma.orgCategory.upsert({
          where: {
            organisationId_categoryId: {
              organisationId: org.id,
              categoryId: categoryMap[catName],
            },
          },
          update: {},
          create: {
            organisationId: org.id,
            categoryId: categoryMap[catName],
          },
        })
      }
    }

    orgCount++

    // Create vacancies for this organisation
    for (const vacData of orgData.vacancies) {
      const vacancy = await prisma.vacancy.create({
        data: {
          title: vacData.title,
          description: vacData.description,
          city: vacData.city,
          location: vacData.location,
          remote: vacData.remote,
          hours: vacData.hours,
          duration: vacData.duration,
          status: "ACTIVE",
          organisationId: org.id,
        },
      })

      // Add vacancy skills
      for (const skillName of vacData.skills) {
        if (skillMap[skillName]) {
          await prisma.vacancySkill.upsert({
            where: {
              vacancyId_skillId: {
                vacancyId: vacancy.id,
                skillId: skillMap[skillName],
              },
            },
            update: {},
            create: {
              vacancyId: vacancy.id,
              skillId: skillMap[skillName],
            },
          })
        }
      }

      // Add vacancy categories
      for (const catName of vacData.categories) {
        if (categoryMap[catName]) {
          await prisma.vacancyCategory.upsert({
            where: {
              vacancyId_categoryId: {
                vacancyId: vacancy.id,
                categoryId: categoryMap[catName],
              },
            },
            update: {},
            create: {
              vacancyId: vacancy.id,
              categoryId: categoryMap[catName],
            },
          })
        }
      }

      vacancyCount++
    }
  }

  console.log(`‚úÖ Done!`)
  console.log(`   üìÇ ${CATEGORIES_DATA.length} categorie√´n aangemaakt`)
  console.log(`   üîß ${SKILLS_DATA.length} vaardigheden aangemaakt`)
  console.log(`   üè¢ ${orgCount} organisaties aangemaakt`)
  console.log(`   üìã ${vacancyCount} vacatures aangemaakt`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
